/*
Una variable constante es una variable que nunca cambia su valor
Para declarar una variable constante es necesario siempre inicializarla
*/

const PI = 3.1416;
console.log(PI);

PI = 15;    //Error, una constante no puede cambiar su valor
console.log(PI);

